export function generateHeader(pFileName: string): string {
	const indexOfFileNameSymbol = HEADER.indexOf(HEADER_FILENAME_SYMBOL);
	if (indexOfFileNameSymbol === -1) {
		throw new Error(`Couldn't find \`${HEADER_FILENAME_SYMBOL}\` inside the HEADER string.`);
	}

	return `${
		HEADER.slice(0, indexOfFileNameSymbol) + pFileName + HEADER.slice(indexOfFileNameSymbol + pFileName.length)
	}\n`;
}

export function generateDoNotModifyByHand(): string {
	return "// DO NOT MODIFY THIS FILE BY HAND - IT IS AUTO GENERATED BY `emscripten-builder-externalglobaldts`.";
}

export function generateDisableEslintComment(pRules: string[]): string {
	return `/* eslint-disable ${pRules.join(", ")} -- Auto-generated file. */`;
}
